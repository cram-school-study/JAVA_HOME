// 'Ctrl' + '+' + '-'  - 프로그램 창 폰트 키우고 줄이기
// 'Ctrl' + '/' - 키를 누르면 주석이 토글된다.
// 'Ctrl' + 'Alt' + 화살표 업, 다운키를 누르면 한줄 복사 - intel 그래픽카드의 경우는 화면이 뒤집어지니 따로 설정이 필요 (바탕화면 - 그래픽 속성 - 바로가기 키 - 사용안함)
// 'Ctrl' + 'I' - 코드 자동 들여쓰기
// 'Alt' + 'Shift' + 'A' - 멀티컬럼을 편집할 수 있다. 이때 화면 자동으로 축소한다. Shift 키를 이용 컬럼 선택, 해제 다시 한번 같은키